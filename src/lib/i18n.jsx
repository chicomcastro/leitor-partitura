const LOCALES = [
  { code: 'pt-BR', name: 'Português', flag: 'PT' },
  { code: 'en', name: 'English', flag: 'EN' },
]

const I18nContext = createContext()

export function I18nProvider({ children }) {
  const [locale, setLocaleState] = useState(() => localStorage.getItem('sp.locale') || 'pt-BR')

  const t = useCallback((key) => {
    return translations[locale]?.[key] || translations['pt-BR'][key] || key
  }, [locale])

  const changeLocale = useCallback((code) => {
    localStorage.setItem('sp.locale', code)
    setLocaleState(code)
  }, [])

  return (
    <I18nContext.Provider value={{ t, locale, changeLocale, LOCALES }}>
      {children}
    </I18nContext.Provider>
  )
}

export function useI18n() {
  return useContext(I18nContext)
}
